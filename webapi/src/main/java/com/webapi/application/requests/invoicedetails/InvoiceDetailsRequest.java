
package com.webapi.application.requests.invoicedetails;

import com.webapi.application.abstractions.IRequest;

public class InvoiceDetailsRequest implements IRequest {
    private int userId;
    private int id;
    private String role;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
